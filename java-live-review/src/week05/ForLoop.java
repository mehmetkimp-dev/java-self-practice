package week05;

public class ForLoop {
    public static void main(String[] args) {

        for (int i = 1; i <=10 ; i++) {
            System.out.println("Hello Friends");
        }

        for (int i = 1; i <=5 ; i++) {
            System.out.print("java - "+i+ " ");
        }
        for (int i = 9; i >=1 ; i--) {
            System.out.println("selenium - " +i);

        }

    }
}
