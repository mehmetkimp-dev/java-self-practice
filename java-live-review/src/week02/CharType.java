package week02;

public class CharType {
    public static void main(String[] args) {

        char first = 'J';
        char second = 'a';

        char third = 118;   // this means 'v' --> ASCII CODE
        char fourth = 97;   // this means 'a' --> ASCII CODE
        System.out.println(first);
        System.out.println(second);
        System.out.println(third);
        System.out.println(fourth);

        System.out.println(""+first+"\n"+second+third+fourth); // if we want to go to new line
                                                                // "\n" (n must be om double quote("")

    }
}
