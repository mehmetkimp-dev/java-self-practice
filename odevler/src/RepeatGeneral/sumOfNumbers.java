package RepeatGeneral;

public class sumOfNumbers {
    public static void main(String[] args) {

        int[] numbers={5,10,25,30};

        int sum=0;

        for (int i = 0; i < numbers.length; i++) {
            sum=sum+numbers[i];

        }
        System.out.println("sum = " + sum);


    }
}



