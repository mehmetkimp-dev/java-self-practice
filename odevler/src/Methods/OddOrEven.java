package Methods;

public class OddOrEven {
    public static void main(String[] args) {

        oddOrEvenNumber(45);


    }

    public static void oddOrEvenNumber(int number) {
        if (number % 2 == 0) {
            System.out.println(number + " is even number ");
        } else {
            System.out.println(number + " is odd number");
        }


    }


}
